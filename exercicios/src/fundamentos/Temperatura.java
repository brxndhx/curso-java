package fundamentos;

public class Temperatura {
	public static void main(String[] args) {
		
		double fare = 86;
		final int x = 32;
		final double y = 5/9.0;
		double cel = (fare - x) * y;
		
		
		System.out.println(fare + "°F quando convertidos são " + cel + "em °C"); 
		
		
	}

}
